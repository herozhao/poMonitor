	private static String findCharset(Map<String, List<String>> map) {
		List<String> list = map.get("Content-Type");
		String charset = "utf-8";
		for (String s : list) {
			String[] sarray = s.split(";");
			for (String ss : sarray) {
				if (ss.contains("charset=")) {
					charset = ss.substring(9);
					System.out.println("编码集是：" + charset);
					return charset;
				}
			}
		}
		return charset;
	}

